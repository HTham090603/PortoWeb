function showProductDetails(productKey) {
    let product = products[productKey];
    localStorage.setItem("product-image", product.image);
    localStorage.setItem("product-name", product.name);
    localStorage.setItem("product-description", product.description);
    localStorage.setItem("product-original", product.price.original);
    localStorage.setItem("product-discount", product.price.discount);
    localStorage.setItem("product-feature1", product.features.feature1);
    localStorage.setItem("product-feature2", product.features.feature2);
    localStorage.setItem("product-feature3", product.features.feature3);
    let pk = productKey
    localStorage.setItem("product-key", pk);

    if (event.target.tagName !== 'A')
        window.location.href = '/productdetail.html';
}

function showProductCart(productKey, event) {
    event.stopPropagation()
    let product = products[productKey];
    let image = product.image
    let name = product.name
    let discount = product.price.discount
    let dataPackage = {
        name: name,
        image: image,
        discount:discount
    }

    var existingInfo = localStorage.getItem('dataPackage');
    if (existingInfo) {
        var existingData = JSON.parse(existingInfo);
        existingData.push(dataPackage);
        localStorage.setItem('dataPackage', JSON.stringify(existingData));
    } else {
        localStorage.setItem('dataPackage', JSON.stringify([dataPackage]));
    }
    window.location.href = '/cart.html';
}
